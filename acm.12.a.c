#define N 305
/**
 * 约瑟夫问题求解函数
 * 该程序模拟一个经典的约瑟夫环问题：n个人围成一圈，从第一个人开始报数，
 * 每次报到m的人出列，然后从下一个人重新开始报数，直到只剩下最后一人。
 * 
 * 算法思路：
 * 1. 初始化一个数组表示n个人，数组值为人员编号
 * 2. 使用循环数组的方式模拟人员围成圈
 * 3. 按照规则依次淘汰人员，直到只剩一人
 * 4. 输出最后剩余的人员编号
 * 
 * 输入格式：多组测试数据，每组包括两个整数n和m
 *           n表示总人数(1<=n<=100)，m表示报数间隔
 *           当n=0且m=0时结束输入
 * 输出格式：对于每组输入，输出最后剩余的人员编号
 */
int main(){
    int a[N], n, m, i, cn,pm;
    while(1){
        scanf("%d%d", &n, &m);
        if(n == 0 && m == 0)
            break;
        // 初始化数组，a[i]存储第i个位置上的人的编号(1~n)
        for(i = 0; i < n; i++)
            a[i] = i+1;
        cn=0;i=-1;pm=0;
        // 循环淘汰人员，直到只剩一人
        while(cn!=n-1){
            i++;
            if(i==n)
                i=0;
            // 如果当前位置上的人未被淘汰
            if(a[i]!=0){
                pm=pm+1;
                // 报数达到m时，淘汰此人
                if(pm==m){
                    a[i]=0;  // 标记为已淘汰
                    pm=0;    // 重置报数器
                    cn=cn+1; // 增加已淘汰人数计数
                }
            }
        }
        // 查找并输出最后一个未被淘汰的人
        for(i=0;i<n;i++){
            if(a[i]!=0){
                printf("%d\n",a[i]);
                break;
            }
        }
        
        
    }
    return 0;
}